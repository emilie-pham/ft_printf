#include "libft.h"

char	*ft_strrev(char *str)
{
	int		i;
	int		j;
	char	k;

	i = 0;
	j = 0;
	while (str[i + 1] != '\0')
		i++;
	while (j < i)
	{
		k = str[j];
		str[j] = str[i];
		str[i] = k;
		j++;
		i--;
	}
	return (str);
}
